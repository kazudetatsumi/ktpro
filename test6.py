# ------------------------------ Data container ------------------------------
@dataclass(frozen=True)
class Structure:
    """Lightweight container for a crystal structure.

    axis : (3,3) Å
    positions : (N,3) fractional
    types : list[int] (len=N)  # per-atom species index, [0, 0, 0, 1, 1, 1]
    n_atoms : list[int] (len=T) # [3, 3]
    elements : list[str] (len=T)  # ['Pd', 'Pd', 'Pd', 'H', 'H', 'H']
    """
    axis: np.ndarray
    positions: np.ndarray
    types: List[int]
    n_atoms: List[int]
    elements: List[str]
    name: str
    comment: str
    volume: float
    # Utility used just before put structure to pypolymlp, as
    # replace of function generate_PolymlpStructure_from_Structure

    def to_pypolymlp(self) -> Dict[str, Any]:
        pos_frac_T = self.positions.T  # (3,N) view（コピー不要）
        return PolymlpStructure(
                axis=np.asarray(self.axis, float),
                positions=pos_frac_T,                 # (3,N)
                n_atoms=list(self.n_atoms),
                types=list(self.types),
                elements=list(self.elements),
                name=self.name,
                comment=self.comment,
                volume=self.volume,
        )


# ------------------------------ POSCAR I/O ------------------------------
class PoscarReader:
    """Read POSCAR/CONTCAR and return a normalized :class:`Structure`.

    The returned positions are fractional.
    """

    def read(self, path: str) -> Tuple[Structure, dict]:
        with open(path, "r", encoding="utf-8") as f:
            lines = f.readlines()
        scale = float(lines[1].split()[0])
        lat = (
            np.array(
                [list(map(float, lines[2 + i].split()[:3])) for i in range(3)]
            )
            * scale
        )
        lines[1] = "   1.00000000000000\n"
        elements_by_types = lines[5].split()
        n_atoms = list(map(int, lines[6].split()))
        elements = [e for i, e in enumerate(elements_by_types)
                    for _ in range(n_atoms[i])]
        types = [i for i, c in enumerate(n_atoms) for _ in range(c)]
        #if "Di" in lines[7] or "di" in lines[7]:
        #    iniline = 8
        #elif "Sel" in lines[7] or "sel" in lines[7]:
        #    iniline = 9
        #else:
        #    iniline = 8  # default fallback
        if "Sel" in lines[7] or "sel" in lines[7]:
            del lines[7]
        iniline = 8
        pos = np.array(
            [
                list(map(float, lines[iniline + i].split()[:3]))
                for i in range(len(types))
            ]
        )
        volume = float(abs(np.linalg.det(lat)))
        s = Structure(axis=lat, positions=pos, types=types, n_atoms=n_atoms,
                      elements=elements, name=lines[0], volume=volume,
                      comment='generated by test5.py')
        meta = {"lines": lines, "iniline": iniline}
        return s, meta


# ------------------------------ Symmetry ------------------------------
class SymmetryAnalyzer:
    """Thin spglib wrapper to obtain primitive/refined cells and
    operations.
    """

    def __init__(self, prim: bool = True, symprec: float = 1e-5):
        self.prim = prim
        self.symprec = symprec

    def refine(self, s: Structure) -> Tuple[Structure, dict]:
        """Return primitive/refined cell with positions sorted by
        species.
        """
        cell = (s.axis, s.positions, np.asarray(s.types, int))
        if self.prim:
            lat, pos, typ = spglib.find_primitive(cell)
        else:
            lat, pos, typ = spglib.refine_cell(cell)
        idx = np.argsort(typ)

        volume = float(abs(np.linalg.det(lat)))
        s2 = Structure(
            axis=lat, positions=pos[idx], types=typ[idx], n_atoms=s.n_atoms,
            elements=s.elements, comment=s.comment, name=s.name, volume=volume,
        )
        return s2, {"sorted_index": idx}

    def operations(self, s: Structure) -> Tuple[np.ndarray, np.ndarray]:
        """Return symmetry rotations and translations for the
        structure.
        """
        cell = (s.axis, s.positions, s.types)
        sym = spglib.get_symmetry(cell, symprec=self.symprec)
        return sym["rotations"], sym["translations"]


