def _gindex_from_Gs(Gs: np.ndarray, nx: int
                    ) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Map G vectors to FFT grid indices (same as previous class code)."""
    gmod = (np.asarray(Gs, int) % int(nx)).astype(int)
    return gmod[:, 0], gmod[:, 1], gmod[:, 2]


def densities_from_states(
    U: np.ndarray,
    state_indices: Sequence[int],
    Gs: np.ndarray,
    nx: int,
) -> np.ndarray:
    """
    Return densities |psi_n(r)|^2 for multiple states.
    Assumes U[:, n] is already sorted by ascending energy.

    Returns:
        rho : (n_states, nx, nx, nx)
    """
    gi, gj, gk = _gindex_from_Gs(Gs, nx)

    # Output buffer
    out = np.empty((len(state_indices), nx, nx, nx), dtype=np.float64)

    for i, st in enumerate(state_indices):
        # Fill G-grid
        X = np.zeros((nx, nx, nx), dtype=np.complex128)
        X[gi, gj, gk] = np.asarray(U[:, st], np.complex128, copy=False)

        # FFT → ψ(r)
        psi_r = np.fft.ifftn(X)

        # Density
        out[i] = np.real(psi_r * np.conj(psi_r))

    return out


