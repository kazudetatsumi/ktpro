# -------- total energies and atomic forces calculation by pypolymlp ----------
def pypolymlp_eval_batch(polymlp_opts: dict, r_st: Structure,
                         irr_pts: np.ndarray,
                         center_frac,
                         ) -> np.ndarray:
    """
    Total energies and atomic forces of the structues with the target H atom
    at the mesh points are calculated by pypolymlp. The structures are stored
    in a list of pypolymlp structure dataclass and their energies are obtained
    at one execution of pypolymlp.
    """
    hidx = get_hindex(r_st, center_frac)
    pypolymlporgstructures = [r_st.to_pypolymlp()]
    polymlp = invoke_polymlp(polymlp_opts, r_st)
    new_structs = []
    for hpos in irr_pts:
        s = copy.deepcopy(polymlp.structures[0])
        s.positions[:, hidx] = hpos
        new_structs.append(s)
    polymlp.structures = new_structs
    eval_out = polymlp.eval()
    energies = np.asarray(eval_out[0])
    forces = np.asarray(eval_out[1])
    polymlp.structures = pypolymlporgstructures
    return energies, forces


